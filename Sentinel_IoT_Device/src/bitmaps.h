/*
 * Project:  Saucemaker's Sentinel
 * File:     bitmaps.h  —  pixel art icons for the OLED display
 * Author:   Nicole
 * Date:     03/07/2026
 *
 * Each bitmap is a 2D grid of bits packed into bytes.
 * A 1-bit = white pixel, 0-bit = black (off) pixel.
 * Each row of the image is one horizontal strip of bytes.
 *
 * 16x16 icons: 2 bytes per row x 16 rows = 32 bytes each.
 * 40x40 icons: 5 bytes per row x 40 rows = 200 bytes each.
 *
 * To use a bitmap on the OLED:
 *   display.drawBitmap(x, y, BITMAP_NAME, width, height, WHITE);
 *
 * ── ACTIVE SET (used in the current UI) ─────────────────────────
 *   BITMAP_THERMO16   — thermometer, 16x16 (idle + alert screens)
 *   BITMAP_HOURGLASS16 — hourglass, 16x16  (cycle in progress)
 *   BITMAP_SHIELD16   — shield,    16x16  (unused, archived)
 *   BITMAP_CANCEL16   — circle+X,  16x16  (cancelling screen)
 *   BITMAP_POT        — cooking pot, 40x40 (loading screen)
 *   BITMAP_SHIELD     — detailed shield with checkmark, 40x40 (success)
 *
 * ── ARCHIVE (kept for future use or larger displays) ─────────────
 *   BITMAP_THERMO     — detailed thermometer, 40x40
 *   BITMAP_CANCEL     — detailed cancel swirl, 40x40
 *   BITMAP_SUCCESS    — checkmark in circle,  40x40
 */

